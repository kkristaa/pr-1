a = 20 #Змінна А
b = 6 #Змінна В

print("БАЗОВИЙ КАЛЬКУЛЯТОР:") #Вивід результатів
print(a, "+", b, "=", a + b) #Додавання змінних
print(a, "-", b, "=", a - b) #Віднімання змінних
print(a, "*", b, "=", a * b) #Множення змінних
print(a, "/", b, "=", a / b)   #Ділення змінних
print(a, "//", b, "=", a // b) #Цілочисельне ділення змінних
print(a, "**", b, "**", a ** b) #Підведення до степеня змінних
print("-" * 30)

#Використання складених операторів
print("РОБОТА З ЛІЧИЛЬНИКОМ::") #Вивід результатів
score = 0
print("Початкове число:", score)
score += 10
print("+ 10 :", score) #Додали 10
score += 5
print("+ 5 :", score) #Додали ще 5
score -= 3
print("-3 :", score) #Відняли 3
score *= 2
print("*2 :", score) #Помножили на 2
print("-" * 30)

