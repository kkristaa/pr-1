#Пошук екстремумів
math_score = 11 #Оцінка з математики
biology_score = 9 #Оцінка з біології
history_score = 12 #Оцінка з історії

best = max(math_score, biology_score, history_score) #Пошук максимума
worst = min(math_score, biology_score, history_score) #Пошук мімнімума

print("РЕЗУЛЬТАТИ ПОШУКА ЕКСТРЕМУМА") #Вивід результатів
print("Оцінки: 11, 9, 12") #Вивід всіх оцінок
print("Найкращий результат:", best) #Вивід найкращої оцінки
print("Найгірший результат:", worst) #Вивід найгіршої оцінки
print("-" * 30)

